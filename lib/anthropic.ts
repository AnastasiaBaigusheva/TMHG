// Простые ключевые слова для определения кризисных сообщений на фронтенде
// (дополнительная защита, основная логика — в системном промпте модели).
export const CRISIS_KEYWORDS = [
  "суицид",
  "покончить с собой",
  "не хочу жить",
  "убью себя",
  "самоповреждение",
  "порезать себя",
  "причинить себе вред",
];

export function containsCrisisLanguage(text: string): boolean {
  const lower = text.toLowerCase();
  return CRISIS_KEYWORDS.some((kw) => lower.includes(kw));
}
